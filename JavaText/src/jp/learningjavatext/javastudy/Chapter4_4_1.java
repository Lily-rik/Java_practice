package jp.learningjavatext.javastudy;

class Chapter4_4_1 {
	public static void main(String[] args) {
		// 文字列同士の足し算
		// 文字列が結合されて表示される
		System.out.print("Good + morning = ");
		System.out.println("Good" + "morning");
		
		System.out.print("Good +  + morning + . = ");
		System.out.println("Good"+' '+"morning"+'.');
	}
}