package jp.learningjavatext.javastudy;

class Chapter7_1 {

	public static void main(String[] args) {

		System.out.println("Hello!");
		System.out.println("Hello!");
		System.out.println("Hello!");
		System.out.println("Hello!");
		System.out.println("Hello!");
		
		
		// 繰り返し　for
		int i;
		
		for(i = 0; i < 5; i++) {  // ++はインクリメント演算子と呼ばれ、変数の値を増やす = i=i+1
			System.out.println("Hello!");
		}

	}

}
