package jp.learningjavatext.javastudy;

class Chapter4_2_1 {
	public static void main(String[] args) {
		System.out.print("123 + 456 = "); // 文字列として表示される
		System.out.println(123+456); // 計算結果が表示される
		
		System.out.print("123 + 456 + 789 = ");
		System.out.println(123+456+789);
		
		System.out.print("123 - 456 - 789 = ");
		System.out.println(123-456-789);
		
		System.out.print("123 * 456 * 789 = ");
		System.out.println(123*456*789);
	}

}
